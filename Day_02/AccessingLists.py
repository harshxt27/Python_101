#To access elements in a list, you can use indexing.
#Accessing elements in a list
mylist = ["apple", "banana", "cherry"]
print(mylist[0])  # Accessing the first element
print(mylist[1])  # Accessing the second element
print(mylist[2])  # Accessing the third element
print(mylist[-1]) # Accessing the last element

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5])
print(thislist[2:])  # Accessing from index 2 to the end
print(thislist[:4])  # Accessing from the start to index 4

thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[-4:-1])

