#slicing a list
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5])

#slicing a string
b = "Hello, World!"
print(b[2:5])

#slicing a list with negative indices
print(thislist[-4:-1])

#slicing a string with negative indices
print(b[-5:-2])

#Slice To the End
print(thislist[2:])
print(b[2:])

#Negative Indexing
print(thislist[-1])
print(b[-1])

