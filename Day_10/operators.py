# Arithmetic Operators

# Addition
result = 5 + 3
print("5 + 3 =", result)

# Subtraction
result = 5 - 3
print("5 - 3 =", result)

# Multiplication
result = 5 * 3
print("5 * 3 =", result)

# Division
result = 5 / 3
print("5 / 3 =", result)

# Modulus
result = 5 % 3
print("5 % 3 =", result)

# Exponentiation
result = 5 ** 3
print("5 ** 3 =", result)

# Floor Division
result = 5 // 3
print("5 // 3 =", result)
