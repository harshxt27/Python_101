# For Loops
# Example 1: Print numbers from 0 to 4
x = 5
for i in range(5):
    print(i)    
print("x =", x)

# Example 2: Print each character in a string
y = "hello"
for char in "hello":
    print(char)
print("y =", y)

# Example 3: Iterate through a list
fruits = ["apple", "banana", "cherry"]
z = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
print("z =", z)
